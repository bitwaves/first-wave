package ch.parametrix.common.util.ui.swing.contracts;

/**
 * 
 */
public class DeviceDistance {

	private final int dx;
	private final int dy;

	/**
	 * @param dx
	 * @param dy
	 */
	public DeviceDistance(final int dx, final int dy) {
		this.dx = dx;
		this.dy = dy;
	}

	/**
	 * @return the dx
	 */
	public int getDx() {
		return this.dx;
	}

	/**
	 * @return the dy
	 */
	public int getDy() {
		return this.dy;
	}

}
